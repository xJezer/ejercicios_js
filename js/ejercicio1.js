let number = Math.floor(Math.random() * 4);

switch (number) {
    case 0:
        alert("you wont have luck today");
        break;
    case 1:
        alert("you will have a good day");
        break;
    case 2:
        alert("you will be lucky today");
        break;

    default:
        alert("what happened? you are not lucky today");
        break;
}